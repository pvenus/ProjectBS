

public enum ProblemFocus
{
    None,

    // My immediate survival or direct self-preservation is the top issue.
    SelfPreservation,

    // An ally's crisis is being internalized as a problem I need to respond to.
    AllyRescue,

    // The battlefield flow feels unstable and needs to be calmed or controlled.
    PressureMitigation,

    // No urgent danger dominates, so pressure can be applied proactively.
    OffensivePressure,

    // A meaningful chance is slipping away unless action is taken now.
    OpportunityCapture
}